    public void Show_Pcs_In_JTable()
    {
        ArrayList<pc> list = getPcList();
        
        DefaultTableModel model = (DefaultTableModel)jTable_Display_Pcs.getModel();
        Object[] row = new Object[4];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getWWS_Barcode();
            row[1] = list.get(i).getBFW_Barcode();
            row[2] = list.get(i).getHersteller();
            row[3] = list.get(i).getModell();
            
            model.addRow(row);
        }
    }
    
