    public ArrayList<pc> getPcList()
    {
        ArrayList<pc> pcList = new ArrayList<pc>();
    //    Connection connection = getConnection();
        Connection connection = DBConnection.getConnection2();
        
        
        String query = "SELECT * FROM `pcs` ";
        Statement st;
        ResultSet rs;
        
        try
        {
            st = connection.createStatement();
            //rs = st.executeQuery(query);
            rs = st.executeQuery(query);
            pc PC;
            
            while(rs.next())
            {
                PC = new pc(rs.getInt("wws_barcode"), rs.getInt("bfw_barcode"), rs.getString("hersteller"), rs.getString("modell"));
                pcList.add(PC);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return pcList;
    }
    
